void	ft_is_negative(int n)
{
	char	i;
	char	j;

	i = 78;
	j = 80;
	if (n < 0)
	{
		write (1, &i, 1);
	}
	else
	{
		write (1, &j, 1);
	}
	write (1, "\n", 1);
}
