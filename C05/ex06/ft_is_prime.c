int	ft_is_prime(int nb)
{
	long int	i;
	long int	l;

	l = nb;
	i = 2;
	if (l <= 1)
		return (0);
	while (l/i >= i)
	{
		if (l % i == 0)
			return (0);
		i++;
	}
	return (1);
}
