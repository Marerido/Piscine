void	ft_rev_int_tab(int *tab, int size)
{
	int	c;
	int	i;
	int	j;

	i = 0;
	j = size - 1;
	while (i < j)
	{
		c = tab[i];
		tab[i] = tab[j];
		tab[j] = c;
		++i;
		--j;
	}
}
